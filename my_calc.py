def add_numbers(num_1, num_2):
    result = num_1 + num_2

    return result

def subtract_numbers(num_1, num_2):
    result = num_1 - num_2

    return result

def multiply_numbers(num_1, num_2):
    result = num_1 * num_2

    return result

def divide_numbers(num_1, num_2):
    result = num_1 / num_2

    return result